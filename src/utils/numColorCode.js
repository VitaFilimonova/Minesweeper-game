export const numColorCode = (num) => {
  switch (num) {
    case 1:
      return "#1976d2";
    case 2:
      return "#388d3c";
    case 3:
      return "#d33030";
    case 4:
      return "#051075";
    case 5:
      return "#835b06";
    case 6:
      return "#04e0cb";
    case 7:
      return "#000000";
    case 8:
      return "#ffffff";
    default:
      return;
  }
};
